'''Como usuário quero informar a média do aluno para saber se ele foi aprovado. 
• Critério 1 – Dados de Entrada: media 
• Critério 2 – Condição: se a media >5, mostrar a Mensagem 1. 
• Critério 3 – Mensagem 1: “Aluno aprovado!” '''

